    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(lblPic.getIcon()==null && lblPassport.getIcon()==null && lblVisa.getIcon()==null)
        {
            JOptionPane.showMessageDialog(rootPane,"Please Browse the Documents then press 'Save' button");
            
        }
        else
        {
            try
            {
                connection c=new connection();
                Connection con=c.conn();
                PreparedStatement ps=con.prepareStatement("insert into tbl_documents(empId,pic,passportPic,visaPic) values(?,?,?,?)");
                ps.setInt(1,empId);
                ps.setBinaryStream(2,finPic,lenPic);
                ps.setBinaryStream(3,finPassport,lenPassport);
                ps.setBinaryStream(4,finVisa,lenVisa);
                int status=ps.executeUpdate();
                if(status>0)
                {
                    dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane,"Error");
                }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(rootPane,e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

