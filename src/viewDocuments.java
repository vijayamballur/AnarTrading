    public static BufferedImage resize(BufferedImage image, int width, int height) 
    {
        BufferedImage bi = new BufferedImage(width, height, BufferedImage.TRANSLUCENT);
        Graphics2D g2d = (Graphics2D) bi.createGraphics();
        g2d.addRenderingHints(new RenderingHints(RenderingHints.KEY_RENDERING, RenderingHints.VALUE_RENDER_QUALITY));
        g2d.drawImage(image, 0, 0, width, height, null);
        g2d.dispose();
        return bi;
    }
    public void viewDocuments()
    {
        connection c=new connection();
        Connection con=c.conn();
        try
        {

            PreparedStatement ps=con.prepareStatement("select pic,passportPic,visaPic from tbl_documents where empId=?");
            ps.setInt(1, empId);
            ResultSet rs=ps.executeQuery();
            while(rs.next())
            {
                picByte=rs.getBytes(1);
                passportByte=rs.getBytes(2);
                visaByte=rs.getBytes(3);
                ByteArrayInputStream bisPic = new ByteArrayInputStream(picByte);
                ByteArrayInputStream bisPassport = new ByteArrayInputStream(passportByte);
                ByteArrayInputStream bisVisa = new ByteArrayInputStream(visaByte);
                try 
                {
                    bfImage=ImageIO.read(bisPic);
                    resizedImage=resize(bfImage,450,250);
                    imageIcon=new ImageIcon(resizedImage);
                    lblPic.setIcon(imageIcon);
                    
                    bfImage=ImageIO.read(bisPassport);
                    resizedImage=resize(bfImage,450,550);
                    imageIcon=new ImageIcon(resizedImage);
                    lblPassport.setIcon(imageIcon);
                    
                    bfImage=ImageIO.read(bisVisa);
                    resizedImage=resize(bfImage,450,550);
                    imageIcon=new ImageIcon(resizedImage);
                    lblVisa.setIcon(imageIcon);
                    
                    
                } 
                catch(IOException e)
                {
                    
                }
             }
            
        }
        catch(Exception e)
        {
            
        }
    }
