    public void jtableSelection()
    {
        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent e) {
            int rowNo=jTable1.getSelectedRow();
            masterId=Integer.parseInt(jTable1.getValueAt(rowNo,0).toString()); 
            viewChildTable();
            }
        });
    }
    public void viewParentTable()
    {
        connection c=new connection();
        Connection con=c.conn();
        try
        {
            Statement stmt=con.createStatement();
            ResultSet rs=stmt.executeQuery("select * from tbl_invoice_parent");
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            jTable1.setAutoCreateRowSorter(true);
            con.close();
        }
        catch(Exception e)
        {

        }
    }
    public void viewChildTable()
    {
        connection c=new connection();
        Connection con=c.conn();
        try
        {
            Statement stmt=con.createStatement();
            ResultSet rs=stmt.executeQuery("select * from tbl_invoice_child where invoiceMasterId="+masterId);
            jTable2.setModel(DbUtils.resultSetToTableModel(rs));
            jTable2.setAutoCreateRowSorter(true);
            con.close();
        }
        catch(Exception e)
        {

        }
    }

