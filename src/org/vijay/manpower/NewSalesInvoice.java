    private void cmbClientNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbClientNameActionPerformed
        // TODO add your handling code here:
        try {
            connection c = new connection();
            Connection con = c.conn();
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT DISTINCT address,country,zip_postBox,phone,fax FROM tbl_clientdetails where clientName='"+cmbClientName.getSelectedItem()+"'");
            while (rs.next()) {
                txtAddress.setText(rs.getString("address")+"\n P.O Box: "+rs.getString("zip_postBox")+", "+rs.getString("country")+", Phone: "+rs.getString("phone")+", Fax: "+rs.getString("fax"));        
            }
            con.close();
        } catch (SQLException ex) {
            
        }
    }//GEN-LAST:event_cmbClientNameActionPerformed

