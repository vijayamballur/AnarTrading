    public static BufferedImage resize(BufferedImage image, int width, int height) 
    {
        BufferedImage bi = new BufferedImage(width, height, BufferedImage.TRANSLUCENT);
        Graphics2D g2d = (Graphics2D) bi.createGraphics();
        g2d.addRenderingHints(new RenderingHints(RenderingHints.KEY_RENDERING, RenderingHints.VALUE_RENDER_QUALITY));
        g2d.drawImage(image, 0, 0, width, height, null);
        g2d.dispose();
        return bi;
    }
    public void viewDocuments()
    {
        connection c=new connection();
        Connection con=c.conn();
        try
        {

            PreparedStatement ps=con.prepareStatement("select document from tbl_invoicedocument where invoiceId=?");
            ps.setInt(1, invoiceId);
            ResultSet rs=ps.executeQuery();
            while(rs.next())
            {
                invoiceByte=rs.getBytes(1);
                ByteArrayInputStream bisInvoice = new ByteArrayInputStream(invoiceByte);
                try 
                {                    
                    bfImage=ImageIO.read(bisInvoice);
                    resizedImage=resize(bfImage,663,821);
                    imageIcon=new ImageIcon(resizedImage);
                    lblInvoice.setIcon(imageIcon);                             
                } 
                catch(IOException e)
                {
                    
                }
             }
            
        }
        catch(Exception e)
        {
            
        }
    }
